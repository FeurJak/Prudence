package vm

import (
	"sync"

	"github.com/holiman/uint256"
)

var stackPool = sync.Pool{
	New: func() interface{} {
		return &Stack{data: [512]byte{}}
	},
}

// Stack is an object for basic stack operations. Items popped to the stack are
// expected to be changed and modified. stack does not take care of adding newly
// initialised objects.
type Stack struct {
	data   [512]byte
	offset int

	// DEGEN Stuff
	histEnabled    bool
	hist           []uint256.Int
	snapshots      map[uint64][]uint256.Int
	lastSnapshotAt uint64
}

func newstack() *Stack {
	return stackPool.Get().(*Stack)
}

func returnStack(s *Stack) {
	s.data = s.data[:0]
	stackPool.Put(s)
}

// Data returns the underlying stack
func (st *Stack) Data() *[512]byte {
	return &st.data
}

func (st *Stack) push(d [32]byte) {
	st.data[st.offset+0] = d[0]
	st.data[st.offset+1] = d[1]
	st.data[st.offset+2] = d[2]
	st.data[st.offset+3] = d[3]
	st.data[st.offset+4] = d[4]
	st.data[st.offset+5] = d[5]
	st.data[st.offset+6] = d[6]
	st.data[st.offset+7] = d[7]

	st.offset++
}

func (st *Stack) pop() (ret uint256.Int) {
	ret = st.data[len(st.data)-1]
	st.data = st.data[:len(st.data)-1]
	return
}

func (st *Stack) len() int {
	return st.offset
}

func (st *Stack) swap(n int) {
	st.data[st.len()-n], st.data[st.len()-1] = st.data[st.len()-1], st.data[st.len()-n]
}

func (st *Stack) dup(n int) {
	st.push(&st.data[st.len()-n])
}

func (st *Stack) peek() *uint256.Int {
	return &st.data[st.len()-1]
}

// Back returns the n'th item in stack
func (st *Stack) Back(n int) *uint256.Int {
	return &st.data[st.len()-n-1]
}
